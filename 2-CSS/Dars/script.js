let timeoutDelay = 300;
let onChangeDelay;
/////////////////////////////////////////////////////// 1 \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\
var editHtml_1 = CodeMirror.fromTextArea(document.getElementById("editHtml_1"),{
    lineNumbers: true,
    styleActiveLine: true,
    mode: "htmlmixed",
    theme: "panda-syntax",
});
var editCss_1 = CodeMirror.fromTextArea(document.getElementById("editCss_1"),{
    lineNumbers: true,
    styleActiveLine: true,
    mode: "css",
    theme: "darcula"
});
let updateResult_1 = function() {
let result_1 = document.querySelector('#result_1');
let resultDocument = result_1.contentDocument || result_1.contentWindow.document;
resultDocument.open();
resultDocument.write(editHtml_1.getValue());
resultDocument.close();
resultDocument.querySelector('head').innerHTML = '<style>' + editCss_1.getValue() + '</style>';
}

let editorOnChange_1 = function() {
    clearTimeout(timeoutDelay);
    onChangeDelay = setTimeout(updateResult_1, timeoutDelay);
};
editCss_1.on('change', editorOnChange_1);
editHtml_1.on('change', editorOnChange_1);
setTimeout(updateResult_1, timeoutDelay);
editHtml_1.setSize("100%","400");
editCss_1.setSize("100%","400");
